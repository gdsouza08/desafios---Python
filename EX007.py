n = int(input('Digite um valor: '))
dobro = n * 2
triplo = n * 3
raiz = n ** (1/2)

print('o valor digitado foi {}.'.format(n))
print('O dobro do valor informado é {}.'.format(dobro))
print('O triplo do valor informado é {}.'.format(triplo))
print('A raiz quadrada do valor informado é {:.2f}.'.format(raiz))